namespace DeepNestSharp.Domain
{
  public enum Cursors
  {
    /// <summary>
    /// Removes any override applied.
    /// </summary>
    Null,

    /// <summary>
    /// Gets the System.Windows.Input.Cursor that appears when an application is starting.
    /// </summary>
    AppStarting,

    /// <summary>
    /// Gets the Arrow System.Windows.Input.Cursor.
    /// </summary>
    Arrow,

    /// <summary>
    /// Specifies a wait (or hourglass).
    /// </summary>
    Wait,
  }
}
